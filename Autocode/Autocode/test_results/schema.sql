CREATE TABLE employees (
    id BIGINT NOT NULL CHECK (id BETWEEN 1.0 AND 10.0)
    name TEXT NOT NULL CHECK (LENGTH(name) BETWEEN 8 AND 13)
    email TEXT NOT NULL CHECK (LENGTH(email) BETWEEN 20 AND 25)
    age BIGINT NOT NULL CHECK (age BETWEEN 26.0 AND 35.0)
    salary BIGINT NOT NULL CHECK (salary BETWEEN 52000.0 AND 72000.0)
    department TEXT NOT NULL CHECK (LENGTH(department) BETWEEN 2 AND 11)
    is_active BOOLEAN NOT NULL
    hire_date TEXT NOT NULL CHECK (LENGTH(hire_date) = 10)
    PRIMARY KEY (id)
);